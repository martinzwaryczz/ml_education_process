# Utiliza los m√©todos keys(), values(), y items() en el diccionario persona para imprimir las claves, los valores y los pares clave-valor.

frutas = {
    "manzana":"rojo",
    "banana":"amarillo",
    "durazno":"naranja",
    "kiwi":"verde"
}

print(f"Las claves del diccionario son: {frutas.keys()} \n")  
print(f"Los valores del diccionario son: {frutas.values()} \n")
print(f"Los items son: {frutas.items()}")